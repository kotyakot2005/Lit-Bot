def start_test(chat_id, work_key):
    work = works.get(work_key)
    if not work:
        bot.send_message(chat_id, "❌ Произведение не найдено")
        return
    
    questions = work.get("tests", [])
    if not questions:
        bot.send_message(chat_id, "❓ Тест для этого произведения пока не готов!")
        return
    
    user_tests[chat_id] = {
        "work_key": work_key,
        "current": 0,
        "score": 0,
        "total": len(questions)
    }
    send_next_question(chat_id)

def send_next_question(chat_id):
    test_data = user_tests.get(chat_id)
    if not test_data:
        return
    
    current = test_data["current"]
    total = test_data["total"]
    
    if current >= total:
        score = test_data["score"]
        work_key = test_data["work_key"]
        
        result = f"✅ *ТЕСТ ЗАВЕРШЁН!*\n\n📚 {works[work_key]['full_title']}\n📊 Результат: *{score}/{total}*"
        bot.send_message(chat_id, result, parse_mode="Markdown")
        del user_tests[chat_id]
        return
    
    work_key = test_data["work_key"]
    question = works[work_key]["tests"][current]
    bot.send_message(
        chat_id,
        f"❓ *Вопрос {current + 1}/{total}*\n\n{question['q']}\n\n✏️ *Напиши свой ответ:*",
        parse_mode="Markdown"
    )

@bot.message_handler(func=lambda message: True)
def handle_text_answer(message):
    test_data = user_tests.get(message.chat.id)
    if not test_data:
        return
    
    work_key = test_data["work_key"]
    current = test_data["current"]
    
    if current >= test_data["total"]:
        return
    
    correct = works[work_key]["tests"][current]["a"]
    user_answer = message.text.strip()
    
    if user_answer.lower() == correct.lower():
        test_data["score"] += 1
        bot.reply_to(message, "✅ *ВЕРНО!* Молодец! ✅", parse_mode="Markdown")
    else:
        bot.reply_to(message, f"❌ *НЕВЕРНО!* Правильный ответ: *{correct}* ❌", parse_mode="Markdown")
    
    test_data["current"] += 1
    send_next_question(message.chat.id)
